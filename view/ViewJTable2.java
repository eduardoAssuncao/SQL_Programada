    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Card c = new Card();
        CardDAO cao = new CardDAO();

        c.setCardID(Integer.parseInt(txtCardID.getText()));
        c.setCardName(txtCardName.getText());
        c.setPlayerID(Integer.parseInt(txtPlayerID.getText()));
        cao.create(c);

        txtCardID.setText("");
        txtCardName.setText("");
        txtPlayerID.setText("");

        readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTableCards.getSelectedRow() != -1) {

            Card c = new Card();
            CardDAO cao = new CardDAO();

            c.setCardID((int) jTableCards.getValueAt(jTableCards.getSelectedRow(), 0));
            
            cao.delete(c);

            txtCardID.setText("");
            txtCardName.setText("");
            txtPlayerID.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTableCards.getSelectedRow() != -1) {

            Card c = new Card();
            CardDAO cao = new CardDAO();

            c.setPlayerID(Integer.parseInt(txtCardID.getText()));
            c.setCardName(txtCardName.getText());
            c.setCardID((int) jTableCards.getValueAt(jTableCards.getSelectedRow(), 0));
            cao.update(c);

            txtCardID.setText("");
            txtCardName.setText("");
            txtPlayerID.setText("");

            readJTable();

        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

