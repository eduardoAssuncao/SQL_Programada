    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTableCards.getModel();
        modelo.setNumRows(0);
        CardDAO cdao = new CardDAO();

        for (Card c : cdao.read()) {

            modelo.addRow(new Object[]{
                c.getCardID(),
                c.getPlayerID(),
                c.getCardName()
            });

        }

    }
    public void readJTableForDesc(String Nkn) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTableCards.getModel();
        modelo.setNumRows(0);
        CardDAO cdao = new CardDAO();

        for (Card c : cdao.readForDesc(Nkn)) {

            modelo.addRow(new Object[]{
                c.getCardID(),
                c.getPlayerID(),
                c.getCardName()
            });

        }

    }
    
